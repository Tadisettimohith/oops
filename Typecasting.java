package Practical1;

public class Typecasting {
	public static void main(String[] args) {
		double d=123.45;
		float f=(float)d;
		long l=(long)f;
		short s=(short)l;
		byte b=(byte)s;
		int num= 65;
		char ch= (char)num;
		System.out.println("byte: "+b);
		System.out.println("int to char:" +ch);
		
	}

}
